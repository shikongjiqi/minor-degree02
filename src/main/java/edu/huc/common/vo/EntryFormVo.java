package edu.huc.common.vo;

public class EntryFormVo {
    private int entryFormId;

    private String userName;

    private String majorName;

    private String minorName;

    public int getEntryFormId() {
        return entryFormId;
    }

    public void setEntryFormId(int entryFormId) {
        this.entryFormId = entryFormId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getMajorName() {
        return majorName;
    }

    public void setMajorName(String majorName) {
        this.majorName = majorName;
    }

    public String getMinorName() {
        return minorName;
    }

    public void setMinorName(String minorName) {
        this.minorName = minorName;
    }
}
